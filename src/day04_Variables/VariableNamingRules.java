package day04_Variables;

public class VariableNamingRules {

    public static void main(String[] args) {

        int num = 100;
        //  double num = 200; variable name MUST be unique

        int age = 21;
        String sentence = "My age is:"+age;
        System.out.println("sentence = " + sentence);
        System.out.println("12"+1);
        System.out.println(12+1);
        // My age is: 31

    }
}
/*
Variable Naming Rules:
1.Must be unique for each variable
2.Starts with the letters
3.cannot have special characters other than _ and $
4.cannot use words reserved by java
5.camelCase
 */
